#define IINF 0x3f3f3f3f - 10

template <typename T>
inline bool chmin(T& a, const T& b) {
    bool compare = a > b;
    if (a > b) a = b;
    return compare;
}
template <typename T>
inline bool chmax(T& a, const T& b) {
    bool compare = a < b;
    if (a < b) a = b;
    return compare;
}

int main() {
    cin.tie(nullptr);
    ios_base::sync_with_stdio(false);

    ll N;
    int M;
    cin >> N >> M;
    ll ans = 0;
    vector<int> muril(M, IINF);
    vector<int> murir(M, IINF);

    int l, r;

    rep(i, N) {
        cin >> l >> r;
        l--;
        r--;
        chmin(muril[l], r);
        chmin(murir[r], l+1);
    }

    rep(i, M) {
        int ub = min(muril[i]-1, M-1);
        for (int j = ub; i < j; j--) {
            if (murir[j] < IINF && murir[j] - 1 >= i) {
                ub = murir[j] - 1;
                j = ub;
            }
        }
        ans += ub - i + 1;
    }

    cout << ans << endl;
    return 0;
}
